// Increment Operators
// Two type of Increment 
// 1. Prefix (++a)
// 2. Postfix (a++)


// prefix
var a = 10;
console.log(++a) // First Increases than print
// Output = 11

// postfix
var b = 10;
console.log(b++) // First Print than Increases
// Output = 10

// Decrement Operators
// Two type of Decrement
// 1. Prefix (--a)
// 2. Postfix (a--)

// Prefix

var c = 10;
console.log(--c);
// Output = 9

//  Postfix
var d = 10;
console.log(d--);
// Output = 10


// ** Exponentiation

